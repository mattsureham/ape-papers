source("00_packages.R")

data_dir <- "../data"
tables_dir <- "../tables"
dir.create(tables_dir, showWarnings = FALSE)

## ---------------------------------------------------------------------------
## Load data
## ---------------------------------------------------------------------------

dt <- readRDS(file.path(data_dir, "analysis_data.rds"))
results <- readRDS(file.path(data_dir, "main_results.rds"))
robustness <- readRDS(file.path(data_dir, "robustness_results.rds"))

dt_near <- dt[abs(dist_signed) <= 2000]
dt_near[, log_price_m2 := log(price_m2)]

## ---------------------------------------------------------------------------
## Table 1: Summary Statistics (LaTeX)
## ---------------------------------------------------------------------------

cat("=== Table 1: Summary Statistics ===\n")

stats_all <- dt_near[, .(
  N = format(.N, big.mark = ","),
  `Mean Price/m²` = format(round(mean(price_m2)), big.mark = ","),
  `SD Price/m²` = format(round(sd(price_m2)), big.mark = ","),
  `Median Price/m²` = format(round(median(price_m2)), big.mark = ","),
  `Mean Surface` = round(mean(surface_reelle_bati, na.rm = TRUE), 1),
  `\\% Apartment` = round(100 * mean(type_local == "Appartement"), 1)
)]

stats_rep <- dt_near[rep_side == 1, .(
  N = format(.N, big.mark = ","),
  `Mean Price/m²` = format(round(mean(price_m2)), big.mark = ","),
  `SD Price/m²` = format(round(sd(price_m2)), big.mark = ","),
  `Median Price/m²` = format(round(median(price_m2)), big.mark = ","),
  `Mean Surface` = round(mean(surface_reelle_bati, na.rm = TRUE), 1),
  `\\% Apartment` = round(100 * mean(type_local == "Appartement"), 1)
)]

stats_nonrep <- dt_near[rep_side == 0, .(
  N = format(.N, big.mark = ","),
  `Mean Price/m²` = format(round(mean(price_m2)), big.mark = ","),
  `SD Price/m²` = format(round(sd(price_m2)), big.mark = ","),
  `Median Price/m²` = format(round(median(price_m2)), big.mark = ","),
  `Mean Surface` = round(mean(surface_reelle_bati, na.rm = TRUE), 1),
  `\\% Apartment` = round(100 * mean(type_local == "Appartement"), 1)
)]

tex_tab1 <- c(
  "\\begin{table}[htbp]",
  "\\centering",
  "\\caption{Summary Statistics: Property Transactions Near REP/Non-REP Boundaries}",
  "\\label{tab:summary}",
  "\\begin{adjustbox}{max width=\\textwidth}",
  "\\begin{tabular}{lcccccc}",
  "\\toprule",
  " & N & Mean Price/m\\textsuperscript{2} & SD & Median & Mean Surface & \\% Apt \\\\",
  "\\midrule",
  paste0("All & ", paste(stats_all, collapse = " & "), " \\\\"),
  paste0("REP Side & ", paste(stats_rep, collapse = " & "), " \\\\"),
  paste0("Non-REP Side & ", paste(stats_nonrep, collapse = " & "), " \\\\"),
  "\\bottomrule",
  "\\end{tabular}",
  "\\end{adjustbox}",
  "\\begin{tablenotes}",
  "\\small",
  "\\item \\textit{Notes:} Sample restricted to transactions within 2km of a REP/non-REP coll\\`ege catchment boundary. Prices in euros per square meter. Data: DVF g\\'eolocallis\\'e 2020--2024.",
  "\\end{tablenotes}",
  "\\end{table}"
)

writeLines(tex_tab1, file.path(tables_dir, "table1_summary.tex"))
cat("  Saved table1_summary.tex\n")

